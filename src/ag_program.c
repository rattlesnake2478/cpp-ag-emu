void Scales(void) {
    int cou = 0;
    static uint16 MaxAltitude = 600;
    static float LastAltitude = 0;
    static uint16 MaxSpeed = 200;
    static float Variometer = 0;
    static int16 difAlt = 0;
    float FTemp = 0;

    MaxAltitude = FlyPack.PBit.Altitude1000 ? 1000 : 600;
    if (FlyPack.PBit.Altitude5000)MaxAltitude = 5000;

    MaxSpeed = FlyPack.PBit.Speed300 ? 300 : 200;

    //Bound speed to 10 km/h min
    if (FlyPack.Barspeed < 10) FlyPack.Barspeed = 10;

    //Calculate variometer
    if (tick == 0 || tick == 5) {
        // On the first and fifth cycle calculate vario
        Variometer = Variometer * 0.5 + difAlt * 0.5; //Variometer coeff
        Variometer -= fmod(Variometer, 0.2);
        difAlt = 0;
    } else {
        // otherwise accumulate data
        difAlt += (FlyPack.Altitude - LastAltitude);
        LastAltitude = FlyPack.Altitude;
    }

    if (Variometer > 9.8)Variometer = 9.8;
    if (Variometer<-9.8)Variometer = -9.8;
    if (Variometer>-0.5 && Variometer < 0.5) Variometer = 0.0;

    //Drawing white window for numbers
    Rect(0, 0, 80, 45, COLOR_WHITE);
    Rect(0, 435, 80, 45, COLOR_WHITE);
    Rect(555, 0, 85, 45, COLOR_WHITE);
    Rect(560, 435, 80, 45, COLOR_WHITE);

    //Drawing prepainted masks
    if (FlyPack.PBit.Altitude5000) Mask(480, 0, alt5000, 150, 480);
    else if (FlyPack.PBit.Altitude1000) Mask(480, 0, alt1000, 150, 480);
    else Mask(480, 0, alt600, 150, 480);

    if (FlyPack.PBit.Speed300) Mask(10, 0, vel300, 150, 480);
    else Mask(10, 0, vel200, 150, 480);



    //Draw texts
    OutText("M", 290, 170, COLOR_WHITE, COLOR_BLACK, 20);
    OutText("Km/h", -310, 170, COLOR_WHITE, COLOR_BLACK, 20);
    if (FlyPack.PBit.IsPitchCor && FlyPack.PBit.CorrOn) {
        OutText("T", 145, 220, COLOR_BLACK, COLOR_SNOW, 20);
    }
    if (FlyPack.PBit.IsRollCor && FlyPack.PBit.CorrOn) {
        OutText("K", 130, 220, COLOR_BLACK, COLOR_SNOW, 20);
    }
    // Меняем цвет шкалы, если часы - то белый, если таймер - то зеленый
    if (FlyPack.PBit.TimerMode) {
        Rect(220, 435, 200, 45, TIMER_COLOR);
    } else {
        Rect(220, 435, 200, 45, CLOCK_COLOR);
    }

    Rect(280, 0, 80, 40, COLOR_WHITE);

    //Drawing data in windows
    PrintHugeInt(FlyPack.Barspeed, -255, 202, COLOR_BLACK, RIGHT);
    if (FlyPack.Altitude < 1000) {
        PrintHugeInt((int16) FlyPack.Altitude, 306, 202, COLOR_BLACK, RIGHT);
    } else {
        PrintHugeInt((int16) FlyPack.Altitude, 312, 202, COLOR_BLACK, RIGHT);
    }
    PrintBigInt(FlyPack.PVal.Pressure / 40, 288, -220, -320, 320, -240, 240, COLOR_BLACK, RIGHT);
    PrintBigFloat(fabs(Variometer), -15, 208, COLOR_BLACK);
    PrintBigFloat(fabs(FlyPack.N5g), -300, -231, COLOR_BLACK);
    if (Variometer < -0.05)BigSymbol('-', -35, 208, -320, 320, -240, 240, COLOR_BLACK);
    if (FlyPack.N5g < -0.05)BigSymbol('-', -315, -231, -320, 320, -240, 240, COLOR_BLACK);

    //Filling speed scale
    for (cou = -137; cou<-37; cou += 5) {
        color Temp;
        if (FlyPack.PBit.Speed300) {
            Temp = (((cou + 140) / 3.3 >= FlyPack.PVal.SpeedMin)&&((cou + 140) / 3.3 < FlyPack.PVal.SpeedMax)) ? SCALE_COLOR : DANGER_RED;
        } else {
            Temp = (((cou + 140) / 5 >= FlyPack.PVal.SpeedMin)&&((cou + 140) / 5 < FlyPack.PVal.SpeedMax)) ? SCALE_COLOR : DANGER_RED;
        }
        Fill((int16) (303 * sin((cou) * DEG2RAD)), (int16) (303 * cos((cou) * DEG2RAD)), Temp);
    }

    //Filling alt scale
    for (cou = 127; cou < 142; cou += 5) {
        if (FlyPack.PBit.Altitude5000 && (cou < 137))continue;
        if (FlyPack.PBit.Altitude1000 && (cou < 132))continue;
        Fill((int16) (303 * sin((cou + 1) * DEG2RAD)), (int16) (303 * cos((cou + 1) * DEG2RAD)), FlyPack.PBit.Altitude100Rest ? DANGER_RED : SCALE_COLOR);
    }

    //Drawing alt arrow
    FTemp = FlyPack.Altitude;
    if (FTemp > MaxAltitude)FTemp = MaxAltitude;
    if (FTemp < 0)FTemp = 0;
    GetMatrix(0, 50 - 100 * FTemp / MaxAltitude);
    Triang(-mZ[1]*15 + mZ[2]*220, -mY[1]*15 + mY[2]*220, mZ[1]*15 + mZ[2]*220, mY[1]*15 + mY[2]*220, mZ[2]*300, mY[2]*300, BIGARROW_OUT_COLOR);
    Triang(-mZ[1]*12 + mZ[2]*223, -mY[1]*12 + mY[2]*223, mZ[1]*12 + mZ[2]*223, mY[1]*12 + mY[2]*223, mZ[2]*292, mY[2]*292,
            (FlyPack.PBit.Altitude100Rest ? (FTemp < 100 && tick > 4) ? DANGER_RED : BIGARROW_IN_COLOR : BIGARROW_IN_COLOR));

    //Drawing speed arrow
    FTemp = FlyPack.Barspeed;
    if (FTemp > MaxSpeed)FTemp = MaxSpeed;
    if (FTemp < 0)FTemp = 0;
    GetMatrix(0, 130 + 100 * FTemp / MaxSpeed);
    Triang(-mZ[1]*15 + mZ[2]*220, -mY[1]*15 + mY[2]*220, mZ[1]*15 + mZ[2]*220, mY[1]*15 + mY[2]*220, mZ[2]*300, mY[2]*300, BIGARROW_OUT_COLOR);
    Triang(-mZ[1]*12 + mZ[2]*223, -mY[1]*12 + mY[2]*223, mZ[1]*12 + mZ[2]*223, mY[1]*12 + mY[2]*223, mZ[2]*292, mY[2]*292,
            (FTemp >= FlyPack.PVal.SpeedMax * 10 || FTemp <= FlyPack.PVal.SpeedMin * 10) && tick > 4 ? DANGER_RED : BIGARROW_IN_COLOR);

    //Drawing alt marker
    if (FlyPack.PBit.AltitudeMarkOn) {
        FTemp = (float) FlyPack.PVal.AltitudeMark;
        if (FTemp > MaxAltitude)FTemp = MaxAltitude;
        if (FTemp < 0)FTemp = 0;
        GetMatrix(0, 50 - 100 * FTemp / MaxAltitude);
        Triang(-mZ[1]*16 + mZ[2]*330, -mY[1]*16 + mY[2]*330, mZ[1]*16 + mZ[2]*330, mY[1]*16 + mY[2]*330, mZ[2]*310, mY[2]*310, MARK_COLOR);
    }

    //Drawing speed marker
    if (FlyPack.PBit.SpeedMarkOn) {
        FTemp = (float) FlyPack.PVal.SpeedMark;
        if (FTemp > MaxSpeed)FTemp = MaxSpeed;
        if (FTemp < 0)FTemp = 0;
        GetMatrix(0, 130 + 100 * FTemp / MaxSpeed);
        Triang(-mZ[1]*16 + mZ[2]*330, -mY[1]*16 + mY[2]*330, mZ[1]*16 + mZ[2]*330, mY[1]*16 + mY[2]*330, mZ[2]*310, mY[2]*310, MARK_COLOR);
    }

    //Drawing variometer arrow
    /*FTemp=Variometer;
    GetMatrix(0,-30*FTemp/10);
    Triang(-mZ[1]*15+mZ[2]*150,-mY[1]*15+mY[2]*150,mZ[1]*15+mZ[2]*150,mY[1]*15+mY[2]*150,mZ[2]*205,mY[2]*205,VARIO_OUT_COLOR);
    Triang(-mZ[1]*12+mZ[2]*153,-mY[1]*12+mY[2]*153,mZ[1]*12+mZ[2]*153,mY[1]*12+mY[2]*153,mZ[2]*200,mY[2]*200,VARIO_IN_COLOR);
     */
    //Drawing variometer triangles
    if (Variometer < 0.5 && Variometer>-0.5) {
        Triang(-79, 220, -40, 240, -40, 201, VARIO_OUT_COLOR);
        Triang(-75, 220, -45, 235, -45, 205, VARIO_IN_COLOR);
        Triang(79, 220, 40, 240, 40, 201, VARIO_OUT_COLOR);
        Triang(75, 220, 45, 235, 45, 205, VARIO_IN_COLOR);
    }
    if (Variometer >= 0.5) {
        Triang(-79, 201, -40, 240, -40, 201, VARIO_OUT_COLOR);
        Triang(-75, 203, -45, 233, -45, 203, VARIO_UP_COLOR);
        Triang(79, 201, 40, 240, 40, 201, VARIO_OUT_COLOR);
        Triang(75, 203, 45, 233, 45, 203, VARIO_UP_COLOR);
    }
    if (Variometer <= -0.5) {
        Triang(-79, 240, -40, 240, -40, 201, VARIO_OUT_COLOR);
        Triang(-75, 238, -45, 238, -45, 208, VARIO_DOWN_COLOR);
        Triang(79, 240, 40, 240, 40, 201, VARIO_OUT_COLOR);
        Triang(75, 238, 45, 238, 45, 208, VARIO_DOWN_COLOR);
    }

}

