void GetMatrix(float Pitch, float Roll) {
#define MAX_Y 240
#define VIEW  24
    float X;

    cPitch = cos(Pitch * DEG2RAD);
    sPitch = sin(Pitch * DEG2RAD);
    cRoll = cos(Roll * DEG2RAD);
    sRoll = -sin(Roll * DEG2RAD);
    X = MAX_Y / sin(VIEW * DEG2RAD)*(cPitch > 0 ? 1 : -1);

    mY[0] = -sPitch * cRoll*X;
    mY[1] = cPitch*cRoll;
    mY[2] = sRoll;
    mZ[0] = sPitch * sRoll*X;
    mZ[1] = -cPitch*sRoll;
    mZ[2] = cRoll;
}

void FillHoriz(float Pitch, float Roll) {
    int16 cou, Y1, Y2, addr, c;
    float fX1, fX2, fY1, fY2, k, b, xa;

    // Корректируем горизонт, чтобы не прыгал.
    if (Roll < 0.1 && Roll > 0) {
        Roll = 0.1;
    }
    if (Roll > -0.1 && Roll < 0) {
        Roll = -0.1;
    }

    GetMatrix(Pitch / PitchDeriv, FlyPack.PBit.IsHorizon ? -Roll : 0);

    fY1 = mY[0] - mY[2]*320;
    fX1 = mZ[0] - mZ[2]*320;
    fY2 = mY[0] + mY[2]*320;
    fX2 = mZ[0] + mZ[2]*320;

    c = cPitch * sRoll > 0;

    Y1 = roun(fY1);
    Y2 = roun(fY2);

    if (Y2 - Y1) {
        k = (fX2 - fX1) / (fY2 - fY1);
        b = fX1 - k*fY1;
        cou = -210;

        while (cou < 210) {
            xa = 320 + k * cou + b;
            if (xa > 530)xa = 530;
            if (xa < 110)xa = 110;

            if (xa == 530) {
                addr = 55;
                if (c) while (addr < xa / 2) PLOT2((239 - cou)*320 + (addr++), SKY_COLOR);
                else while (addr < xa / 2) PLOT2((239 - cou)*320 + (addr++), FlyPack.PBit.HorizColor ? HORIZ1_COLOR : HORIZ2_COLOR);
            } else if (xa == 110) {
                addr = 55;
                if (c) while (addr < 265) PLOT2((239 - cou)*320 + (addr++), FlyPack.PBit.HorizColor ? HORIZ1_COLOR : HORIZ2_COLOR);
                else while (addr < 265) PLOT2((239 - cou)*320 + (addr++), SKY_COLOR);
            } else {
                addr = 110;
                if (c) while (addr < xa) PLOT((239 - cou)*640 + (addr++), SKY_COLOR);
                else while (addr < xa) PLOT((239 - cou)*640 + (addr++), FlyPack.PBit.HorizColor ? HORIZ1_COLOR : HORIZ2_COLOR);
                if (c) while (addr < 530) PLOT((239 - cou)*640 + (addr++), FlyPack.PBit.HorizColor ? HORIZ1_COLOR : HORIZ2_COLOR);
                else while (addr < 530) PLOT((239 - cou)*640 + (addr++), SKY_COLOR);
            }
            cou++;
        }
    } else {
        cou = 30;
        if (Y2<-240)Y2 = -240;
        if (Y2 > 239)Y2 = 239;
        while (cou < 239 - Y2) {
            addr = 55;

            if (c) while (addr < 265) PLOT2(cou * 320 + (addr++), FlyPack.PBit.HorizColor ? HORIZ1_COLOR : HORIZ2_COLOR);
            else while (addr < 265) PLOT2(cou * 320 + (addr++), SKY_COLOR);

            cou++;
        }
        while (cou < 239 - Y2 + 1) {
            addr = 55;
            while (addr < 265)PLOT2(cou * 320 + (addr++), COLOR_WHITE);
            cou++;
        }
        while (cou < 450) {
            addr = 55;
            if (c) while (addr < 265) PLOT2(cou * 320 + (addr++), SKY_COLOR);
            else while (addr < 265) PLOT2(cou * 320 + (addr++), FlyPack.PBit.HorizColor ? HORIZ1_COLOR : HORIZ2_COLOR);
            cou++;
        }
    }

    for (cou = -6; cou < 7; cou++) {
        GetMatrix(Pitch / PitchDeriv + cou*PitchCoef, 0);
        b = cou & 1 ? 60 : 100;
        if (!cou) b = 210;
        if (fabs(mY[0]) < AG_Radius) {
            xa = sqrt(AG_Radius * AG_Radius - mY[0] * mY[0]);
            if (b > xa)b = xa;
            GetMatrix(Pitch / PitchDeriv + cou*PitchCoef, FlyPack.PBit.IsHorizon ? -Roll : 0);

            fY1 = b * mY[2] + mY[0];
            fY2 = -b * mY[2] + mY[0];

            if (fY1 > fY2) {
                fX1 = fY1;
                fY1 = fY2;
                fY2 = fX1;
                fX1 = -b * mZ[2] + mZ[0];
                fX2 = b * mZ[2] + mZ[0];
            } else {
                fX1 = b * mZ[2] + mZ[0];
                fX2 = -b * mZ[2] + mZ[0];
            }
            if (b == 100) {
                PrintBigInt((int16) fabs(cou * 5), roun(110 * mZ[2] + mZ[0]), roun(110 * mY[2] + mY[0]), -210, 210, -210, 210, FlyPack.PBit.BlackNum ? HORIZ_NUMCOLOR1 : HORIZ_NUMCOLOR2, CENTER);
                PrintBigInt((int16) fabs(cou * 5), roun(-130 * mZ[2] + mZ[0]), roun(-130 * mY[2] + mY[0]), -210, 210, -210, 210, FlyPack.PBit.BlackNum ? HORIZ_NUMCOLOR1 : HORIZ_NUMCOLOR2, CENTER);
            }
            Lin((int16) fX1, (int16) fY1, (int16) fX2, (int16) fY2, COLOR_WHITE);
            Lin((int16) fX1, (int16) (fY1 + 1), (int16) fX2, (int16) (fY2 + 1), COLOR_WHITE);
        }

    }
    for (cou = 0; cou < 480; cou++) {
        addr = 55;
        while (addr < 265) {
            if ((addr * 2 - 320)*(addr * 2 - 320)+(240 - cou)*(240 - cou) > AG_Radius * AG_Radius)
                PLOT2(addr + cou * 320, COLOR_BLACK);
            addr++;
        }
    }
}

