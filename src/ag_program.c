void Circle(int16 x, int16 y, uint16 r, color c) {
    int i = 0;
    int j = 0;

    for (i = -(int16) r; i <= r; i++) {
        for (j = -(int16) r; j <= r; j++) {
            if ((i * i + j * j) < (r * r + r) && (i * i + j * j) > (r * r - r)) {
                Plot(x + i, y + j, c);
            }
        }
    }
}

void Lin(int16 x1, int16 y1, int16 x2, int16 y2, color c) {
    int cnt, si, end;
    float deltaX, deltaY, k, f;

    deltaX = x1 - x2;
    deltaY = y1 - y2;

    if (fabs(deltaX) > fabs(deltaY)) {
        k = deltaY / fabs(deltaX);
        f = y1;
        si = (int) ((deltaX > 0) ? -1 : 1);
        cnt = (int) (x1 - si);
        end = x2;

        do {
            cnt += si;
            Plot(cnt, (int) f, c);
            f -= k;
        } while (cnt != end);
    } else if (fabs(deltaY) >= fabs(deltaX)) {
        k = deltaX / fabs(deltaY);
        f = x1;

        si = (int) ((deltaY > 0) ? -1 : 1);
        cnt = (int) (y1 - si);
        end = y2;

        do {
            cnt += si;
            Plot((int) f, cnt, c);
            f -= k;
        } while (cnt != end);
    }
    Plot(x1, y1, c);
}

void VLine(int16 x, int16 y1, int16 y2, color c) {

    int16 cnt;
    signed char si = y1 < y2 ? 1 : -1;
    for (cnt = y1; cnt != y2; cnt += si)
        Plot(x, cnt, c);
    Plot(x, y2, c);
}

void Triang(float X1, float Y1, float X2, float Y2, float X3, float Y3, color c) {
    float fTemp1, fTemp2, deltaX1, deltaY1, deltaX2, deltaY2, k1, k2;
    int16 cou, end;

    if ((X1 >= X2)&&(X1 >= X3)) {
        fTemp1 = X1;
        fTemp2 = Y1;

        if (X2 >= X3) {
            X1 = X3;
            Y1 = Y3;
        } else {
            X1 = X2;
            Y1 = Y2;
            X2 = X3;
            Y2 = Y3;
        }

        X3 = fTemp1;
        Y3 = fTemp2;
    } else if ((X2 >= X1)&&(X2 >= X3)) {
        fTemp1 = X2;
        fTemp2 = Y2;

        if (X3 >= X1) {
            X2 = X3;
            Y2 = Y3;
        } else {
            X2 = X1;
            Y2 = Y1;
            X1 = X3;
            Y1 = Y3;
        }

        X3 = fTemp1;
        Y3 = fTemp2;
    } else if ((X3 >= X1)&&(X3 >= X2)&&(X1 > X2)) {
        fTemp1 = X2;
        fTemp2 = Y2;
        X2 = X1;
        Y2 = Y1;
        X1 = fTemp1;
        Y1 = fTemp2;
    }

    deltaX2 = X3 - X1;
    deltaY2 = Y3 - Y1;
    cou = roun(X2);
    end = roun(X1);

    if (cou != end) {
        deltaX1 = X2 - X1;
        deltaY1 = Y2 - Y1;

        k1 = deltaY1 / deltaX1;
        k2 = deltaY2 / deltaX2;

        fTemp1 = Y2;
        fTemp2 = Y1 + k2 * (X2 - X1);


        while (cou > end) {
            VLine(cou, roun(fTemp1), roun(fTemp2), c);
            fTemp1 -= k1;
            fTemp2 -= k2;
            cou--;
        }
    }

    cou = roun(X2);
    end = roun(X3);

    if (cou != end) {
        deltaX1 = X3 - X2;
        deltaY1 = Y3 - Y2;

        k1 = deltaY1 / deltaX1;
        k2 = deltaY2 / deltaX2;

        fTemp1 = Y2;
        fTemp2 = Y1 + k2 * (X2 - X1);

        while (cou < end) {
            VLine(cou, roun(fTemp1), roun(fTemp2), c);
            fTemp1 += k1;
            fTemp2 += k2;
            cou++;
        }
    }

    /*Line(X1,Y1,X2,Y2);
    Line(X1,Y1,X3,Y3);
    Line(X3,Y3,X2,Y2);  */

}

void Rect(uint16 x1, uint16 y1, uint16 sizex, uint16 sizey, color c) {
    unsigned int addr = x1 / 2 + y1 * 320;
    uint16 cntx = 0, cnty = 0;
    while (cnty < sizey) {
        while (cntx < sizex / 2) {
            PLOT2(addr++, c);
            ++cntx;
        }
        cntx = 0;
        addr += 320 - sizex / 2;
        ++cnty;
    }
}

void Mask(uint16 shiftx, uint16 shifty, const unsigned int *mask, unsigned int sizex, unsigned int sizey) {

    unsigned int cntx = 0;
    unsigned int cnty = 0;
    int addr = shiftx / 2 + shifty * 320;
    int maskaddr = 0;
    while (cnty < sizey) {
        while (cntx < sizex / 2) {
            PLOT2C(addr++, mask[maskaddr++]);
            ++cntx;
        }
        cntx = 0;
        addr += 320 - sizex / 2;
        ++cnty;
    }
}

void Fill(int16 x, int16 y, color c) {
    color now = GetP(x, y);
    Plot(x, y, c);
    if ((GetP(x + 1, y) == now)&&(GetP(x + 1, y) != c)&&((x + 1) < 320))Fill(x + 1, y, c);
    if ((GetP(x - 1, y) == now)&&(GetP(x - 1, y) != c)&&((x - 1)>-320))Fill(x - 1, y, c);
    if ((GetP(x, y + 1) == now)&&(GetP(x, y + 1) != c)&&((y + 1) < 240))Fill(x, y + 1, c);
    if ((GetP(x, y - 1) == now)&&(GetP(x, y - 1) != c)&&((y - 1)>-240))Fill(x, y - 1, c);
}
