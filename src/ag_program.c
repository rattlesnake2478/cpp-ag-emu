    static int showsplash = 0;
    tick++;
    if (tick == 10)tick = 0;
    FILL(COLOR_BLACK);
    if (showsplash) {
        static int splashcount = 0;
        Mask(0, 0, splashscreen, 640, 480);
        if (tick == 9) ++splashcount;
        if (splashcount == 10) showsplash = 0;
    } else {
        FillHoriz(FlyPack.Pitch, FlyPack.Roll);
        DrawCart();
        Scales();
        ShowTimer();
        //Compass();
        //Bubble();
        Menu();
    }



