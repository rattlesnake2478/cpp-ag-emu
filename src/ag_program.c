void PrintHugeInt(int16 val, int16 X, int16 Y, uint16 C, uint16 Alignment) {
    char Array[5] = {'0', 0, 0, 0, 0};
    uint16 pos = 0, num;

    num = (uint16) fabs(val);

    while ((num)&&(pos < 4)) {
        Array[pos++] = (char) ('0' + num % 10);
        num /= (uint16) 10;
    }

    if (!pos)pos = 1;
    if (val < 0)Array[pos++] = '-';
    X -= (int16) (pos * 10 * Alignment);

    while (pos--) {
        HugeSymbol(Array[pos], (int16) (X + 5), Y, C);
        X += (int16) 20;
    }
}

