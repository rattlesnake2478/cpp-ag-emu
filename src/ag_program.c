void OutText(const char * str, int16 X, int16 Y, color C1, color C2, uint16 H) {
    uint16 cou = 0;

    while (str[cou]) {
        Symbol((uint16) str[cou], X, Y, C1, C2, H);
        X += (int16) ((str[cou++] == 32) ? 6 : 12);
    }
}

int16 OutBackText(const char * str, int16 X, int16 Y, color C1, color C2, uint16 H) {
    uint16 cou = 0xFFFF;

    while (str[++cou]) {
    }

    while (cou) {
        Symbol((uint16) str[--cou], X, Y, C1, C2, H);
        X -= (int16) ((str[cou - 1] == 32) ? 6 : 12) + 1;
    }

    return X;
}

void PrintInt(int16 val, int16 X, int16 Y, uint16 C1, uint16 C2, uint16 H, uint16 Alignment) {
    char Array[5] = {'0', 0, 0, 0, 0};
    uint16 pos = 0, num;

    num = (uint16) fabs(val);

    while ((num)&&(pos < 4)) {
        Array[pos++] = (char) ('0' + num % 10);
        num /= (uint16) 10;
    }

    if (!pos)pos = 1;
    if (val < 0)Array[pos++] = '-';
    X -= (int16) (pos * 6 * Alignment);

    while (pos--) {
        Symbol(Array[pos], (int16) (X + 3), Y, C1, C2, H);
        X += (int16) 12;
    }
}

