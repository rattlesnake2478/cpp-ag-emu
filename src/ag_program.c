void PrintBigInt(int16 val, int16 X, int16 Y, int16 XMin, int16 XMax, int16 YMin, int16 YMax, color C, uint16 Alignment) {
    char Array[5] = {'0', 0, 0, 0, 0};
    uint16 pos = 0, num;

    num = (uint16) fabs(val);

    while ((num)&&(pos < 4)) {
        Array[pos++] = (char) ('0' + num % 10);
        num /= (uint16) 10;
    }

    if (!pos)pos = 1;
    if (val < 0)Array[pos++] = '-';
    X -= (int16) (pos * 6 * Alignment);

    while (pos--) {
        BigSymbol(Array[pos], (int16) (X + 4), Y - 12, XMin, XMax, YMin, YMax, C);
        X += (int16) 16;
    }
}

